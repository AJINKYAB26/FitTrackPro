export const getExercisesByCategory = (category) => {
  return exercises.filter((ex) => ex.category === category);
};

export const getExercisesByLevel = (level) => {
  const levelOrder = {
    beginner: 1,
    intermediate: 2,
    advanced: 3,
  };

  return exercises.filter(
    (ex) => levelOrder[ex.difficulty] <= levelOrder[level]
  );
};

export const getFilteredExercises = (category, level) => {
  const levelOrder = {
    beginner: 1,
    intermediate: 2,
    advanced: 3,
  };

  return exercises.filter(
    (ex) =>
      ex.category === category &&
      levelOrder[ex.difficulty] <= levelOrder[level]
  );
};


